function genNode(name, id = null, children = [], disabled) {
    return {
        name: name,
        id: id ?? name,
        children: children,
        disabled: disabled,
    }
}

function genChildNode(obj, disabled) {
    return {
        key: obj.key,
        ...genNode(obj.name, obj.id, [], disabled),
    }
}


function genGamePack(ids){
    return ids.map(x=>genChildNode(x,false))
}

